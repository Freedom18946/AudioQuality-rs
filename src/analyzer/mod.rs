/// 声明 `ffmpeg` 子模块。
/// `pub` 关键字使其成为公共模块，意味着在 `analyzer` 模块外部可以访问
/// `analyzer::ffmpeg`。该模块封装了所有与 FFmpeg 命令行工具的交互逻辑。
pub mod ffmpeg;

/// 声明 `metrics` 子模块。
/// `pub` 关键字使其成为公共模块。该模块定义了项目中用于表示
/// 音频技术指标的核心数据结构，例如 `FileMetrics`。
pub mod metrics;

/// 声明 `scoring` 子模块。
/// `pub` 关键字使其成为公共模块。该模块实现了音频质量评分算法，
/// 包括完整性、动态范围和频谱质量的评估逻辑。
pub mod scoring;

/// 声明 `report` 子模块。
/// `pub` 关键字使其成为公共模块。该模块负责生成CSV报告和排名显示功能。
pub mod report;